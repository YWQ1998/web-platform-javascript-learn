console.log('数据类型');

// int num = 10; java 中声明变量
// var num; // 这里的num 并不确定他的数据类型
var num = 10; // 此处的 num 的数据类型为 数字型
// js 的变量的数据类型是只有程序在运行过程中，根据等号右边的值来确定的
var str = 'pink' // 此处的 str 的数据类型为 字符串
// js 是动语言 变量的数据类型是可以变化的
var x = 10; // 此处 x 的数据类型为数字型
x = 'pink'; // 此处 x 的数据类型为字符串
console.log(x);